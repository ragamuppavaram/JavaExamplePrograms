package java_homework;

public class IfElseTest {
	public static void main(String[] args) {
		int num = -4;

		if (num == 0) {
			System.out.println("The num is zero");
		} else if (num > 0) {
			System.out.println("The num is positive");
		} else {
			System.out.println("The num is negative");
		}
	}
}
